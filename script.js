  const audio = document.createElement("audio")
  const play = document.querySelector("#play")
  const artist = document.getElementById("Artist")
  const img = document.querySelector("#img")
  const slider = document.getElementById("range")
  const title = document.getElementById("title")
  const timer = document.getElementById("timer")
  const timer1 = document.getElementById("timer1")
  const SongDuration = document.getElementById("S_Duration")
  let index = 0
  let play_Song = false

  function Loadtrack(index){
    audio.src = songs[index].src
    title.innerText = songs[index].SongTitle
    img.src = songs[index].img
    artist.innerText = songs[index].ArtistName
    SongDuration.innerText = songs[index].time
    // audio.load()
    audio.play()
  }

  Loadtrack(index)

  function Play(){
    if(play_Song == false) playSong()
    else pauseSong()
  }

  function playSong(){
     audio.load()
    audio.play()
    play_Song = true 
    play.innerHTML = `<i class="fa-solid fa-pause text-white text-lg"></i>`
  }
  function pauseSong(){
    audio.pause()
    play_Song = false
    play.innerHTML = `<i class="fa-solid fa-play text-white text-lg"></i>`
  }

  function Next(){
      if (index < songs.length - 1) index += 1; 
      else index = 0; 
      Loadtrack(index); 
      slider.value = 0; 
      if (play_Song) playSong(); 
      else play.innerHTML = `<i class="fa-solid fa-play text-white text-lg"></i>`;
    }
  
  function Prv(){
    if (index > 0) index -= 1;
    else index = songs.length - 1; 
    Loadtrack(index); 
    if (play_Song) playSong(); 
    else play.innerHTML = `<i class="fa-solid fa-play text-white text-lg"></i>`; 
  }

  slider.addEventListener("input", () => {
    audio.addEventListener("timeupdate", () => {
      if (audio.duration) {
        const progress = (audio.currentTime / audio.duration) * 100;
        slider.value = progress; 
        const currentTimeInMinutes = Math.floor(audio.currentTime / 60);
        const currentTimeInSeconds = Math.floor(audio.currentTime % 60);
        timer.innerText = currentTimeInMinutes < 10 ? `0${currentTimeInMinutes}` : currentTimeInMinutes;
        timer1.innerText = currentTimeInSeconds < 10 ? `0${currentTimeInSeconds}` : currentTimeInSeconds;
      }
    });
    const value = slider.value;
  const newTime = (value / 100) * audio.duration; 
  audio.currentTime = newTime;
    if (audio.readyState > 0) { 
      const slidePosition = audio.duration * (slider.value / 100);
      audio.currentTime = slidePosition;
    } else {
      console.error("Audio metadata not loaded yet.");
    }
  });

  function Auto() {
    
    index++;
    if (index >= songs.length) {
      index = 0; 
    }
    Loadtrack(index);
  }
  audio.addEventListener("ended", () => {
    Auto();
  });
  
 
